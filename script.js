document.addEventListener("click", (event) => {
  const backToTopLink = event.target.closest("[data-scroll-top]");
  if (backToTopLink) {
    event.preventDefault();
    scrollToPageTop();
    return;
  }

  const quoteButton = event.target.closest(".quote-link");
  if (quoteButton) {
    event.stopPropagation();
    if (modal.open) modal.close();
    requestQuote(quoteButton.dataset.product);
    return;
  }

  const catalogCard = event.target.closest("[data-product-id]");
  if (catalogCard) {
    const product = products.find((item) => item.id === catalogCard.dataset.productId);
    if (product) openProductDetail(product);
  }
});

document.addEventListener("keydown", (event) => {
  const catalogCard = event.target.closest("[data-product-id]");
  if (!catalogCard || (event.key !== "Enter" && event.key !== " ")) return;

  event.preventDefault();
  const product = products.find((item) => item.id === catalogCard.dataset.productId);
  if (product) openProductDetail(product);
});

